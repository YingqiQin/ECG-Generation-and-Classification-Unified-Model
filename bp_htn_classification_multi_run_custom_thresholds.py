def evaluate_one_csv(
    path: Path,
    run_id: int,
    methods: List[str],
    average: str,
    true_sbp_col: str,
    true_dbp_col: str,
    subject_col: str,
    sleep_col: str,
    is_calib_col: str,
    include_calib: bool,
    true_thresholds: Dict[str, Threshold],
    cli_pred_thresholds: Dict[str, Threshold],
    loaded_thresholds: Dict[Tuple[str, str], Threshold],
    include_combined: bool,
) -> List[Dict[str, Any]]:
    df0 = pd.read_csv(path)
    df = prepare_df(df0, include_calib=include_calib, is_calib_col=is_calib_col)

    rows: List[Dict[str, Any]] = []
    for method in methods:
        pred_sbp_col, pred_dbp_col = resolve_method_cols(method, df)
        split_labels: Dict[str, Dict[str, Tuple[bool, bool, int]]] = {}

        for split in ["all", "day", "night"]:
            true_thr = true_thresholds[split]
            pred_thr = get_pred_thr(method, split, cli_pred_thresholds, loaded_thresholds)
            m, labels = compute_split_metrics(
                df=df,
                split=split,
                average=average,
                true_thr=true_thr,
                pred_thr=pred_thr,
                subject_col=subject_col,
                sleep_col=sleep_col,
                true_sbp_col=true_sbp_col,
                true_dbp_col=true_dbp_col,
                pred_sbp_col=pred_sbp_col,
                pred_dbp_col=pred_dbp_col,
            )
            if labels is not None:
                split_labels[split] = labels

            row = {
                "run_id": run_id,
                "csv_path": str(path),
                "method": method_name(method),
                "split": split,
                "mode": m.pop("mode"),
                "true_sbp_thr": true_thr.sbp,
                "true_dbp_thr": true_thr.dbp,
                "pred_sbp_thr": pred_thr.sbp,
                "pred_dbp_thr": pred_thr.dbp,
                "true_sbp_col": true_sbp_col,
                "true_dbp_col": true_dbp_col,
                "pred_sbp_col": pred_sbp_col,
                "pred_dbp_col": pred_dbp_col,
            }
            row.update(m)
            rows.append(row)

        if include_combined:
            # Combined is product-level subject OR. It is only meaningful from subject-level split labels.
            # Even if average=micro/macro, we recompute subject-level labels for combined.
            if average != "subject" or len(split_labels) < 3:
                split_labels = {}
                for split in ["all", "day", "night"]:
                    true_thr = true_thresholds[split]
                    pred_thr = get_pred_thr(method, split, cli_pred_thresholds, loaded_thresholds)
                    _, labels = compute_subject_metrics(
                        df=df,
                        split=split,
                        true_thr=true_thr,
                        pred_thr=pred_thr,
                        subject_col=subject_col,
                        sleep_col=sleep_col,
                        true_sbp_col=true_sbp_col,
                        true_dbp_col=true_dbp_col,
                        pred_sbp_col=pred_sbp_col,
                        pred_dbp_col=pred_dbp_col,
                    )
                    split_labels[split] = labels

            m = compute_combined_from_subject_labels(split_labels)
            row = {
                "run_id": run_id,
                "csv_path": str(path),
                "method": method_name(method),
                "split": "combined",
                "mode": m.pop("mode"),
                "true_sbp_thr": np.nan,
                "true_dbp_thr": np.nan,
                "pred_sbp_thr": np.nan,
                "pred_dbp_thr": np.nan,
                "true_sbp_col": true_sbp_col,
                "true_dbp_col": true_dbp_col,
                "pred_sbp_col": pred_sbp_col,
                "pred_dbp_col": pred_dbp_col,
            }
            row.update(m)
            rows.append(row)

    return rows


def summarize_across_runs(per_run: pd.DataFrame) -> pd.DataFrame:
    group_cols = ["method", "split", "mode", "true_sbp_thr", "true_dbp_thr", "pred_sbp_thr", "pred_dbp_thr"]
    metric_cols = ["n_rows", "n_subjects", "tp", "tn", "fp", "fn", "precision", "recall", "sensitivity", "specificity", "accuracy", "f1"]

    rows: List[Dict[str, Any]] = []
    for keys, g in per_run.groupby(group_cols, dropna=False, sort=False):
        if not isinstance(keys, tuple):
            keys = (keys,)
        row = {k: v for k, v in zip(group_cols, keys)}
        row["n_runs"] = int(g["run_id"].nunique())
        for col in metric_cols:
            x = pd.to_numeric(g[col], errors="coerce").dropna().to_numpy(dtype=float)
            if len(x) == 0:
                row[f"{col}_mean"] = np.nan
                row[f"{col}_std_across_runs"] = np.nan
                row[f"{col}_min"] = np.nan
                row[f"{col}_max"] = np.nan
            else:
                row[f"{col}_mean"] = float(np.mean(x))
                row[f"{col}_std_across_runs"] = float(np.std(x, ddof=1)) if len(x) > 1 else np.nan
                row[f"{col}_min"] = float(np.min(x))
                row[f"{col}_max"] = float(np.max(x))
        rows.append(row)
    return pd.DataFrame(rows)


def pooled_confusion(per_run: pd.DataFrame) -> pd.DataFrame:
    group_cols = ["method", "split", "mode", "true_sbp_thr", "true_dbp_thr", "pred_sbp_thr", "pred_dbp_thr"]
    rows: List[Dict[str, Any]] = []
    for keys, g in per_run.groupby(group_cols, dropna=False, sort=False):
        if not isinstance(keys, tuple):
            keys = (keys,)
        row = {k: v for k, v in zip(group_cols, keys)}
        tp = int(pd.to_numeric(g["tp"], errors="coerce").fillna(0).sum())
        tn = int(pd.to_numeric(g["tn"], errors="coerce").fillna(0).sum())
        fp = int(pd.to_numeric(g["fp"], errors="coerce").fillna(0).sum())
        fn = int(pd.to_numeric(g["fn"], errors="coerce").fillna(0).sum())
        m = confusion([True] * tp + [False] * fp + [False] * tn + [True] * fn,
                      [True] * tp + [True] * fp + [False] * tn + [False] * fn)
        row.update({
            "tp_sum": tp,
            "tn_sum": tn,
            "fp_sum": fp,
            "fn_sum": fn,
            "sensitivity_pooled": m["sensitivity"],
            "specificity_pooled": m["specificity"],
            "accuracy_pooled": m["accuracy"],
            "precision_pooled": m["precision"],
            "f1_pooled": m["f1"],
        })
        rows.append(row)
    return pd.DataFrame(rows)


def parse_methods(s: str) -> List[str]:
    return [x.strip() for x in s.split(",") if x.strip()]


def resolve_paths(positional: List[str], csv_glob: Optional[str]) -> List[Path]:
    paths: List[Path] = []
    if csv_glob:
        paths.extend(Path(p) for p in sorted(glob.glob(csv_glob)))
    paths.extend(Path(p) for p in positional)
    seen = set()
    out: List[Path] = []
    for p in paths:
        key = str(p)
        if key not in seen:
            out.append(p)
            seen.add(key)
    if not out:
        raise FileNotFoundError("No CSV files provided. Use --csv-glob or positional csv paths.")
    missing = [str(p) for p in out if not p.exists()]
    if missing:
        raise FileNotFoundError(f"Missing CSV files: {missing[:5]}")
    return out


