#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Multi-run hypertension classification with custom All/Day/Night prediction thresholds.

This script is a practical replacement for bp_htn_classification_multi_run.py when
threshold grid search has produced different prediction thresholds for All/Day/Night.

Default true-label thresholds:
  All   true: 130/80
  Day   true: 135/85  (sleep == 0)
  Night true: 120/70  (sleep == 1)

Prediction thresholds default to the same values, but can be overridden:
  --pred-all-sbp / --pred-all-dbp
  --pred-day-sbp / --pred-day-dbp
  --pred-night-sbp / --pred-night-dbp

It also supports reading thresholds from threshold_grid_best_by_split.csv generated by
the threshold grid-search script. That file should contain:
  method, split, pred_sbp_thr, pred_dbp_thr

Combined rule:
  true_combined = true_all OR true_day OR true_night
  pred_combined = pred_all OR pred_day OR pred_night

Examples:

# Use manual custom thresholds for bias method
python bp_htn_classification_multi_run_custom_thresholds.py \
  --csv-glob "eval_1p0_sensitivity/predictions_*.csv" \
  --methods bias \
  --true-sbp-col ABPM_SBP --true-dbp-col ABPM_DBP \
  --pred-all-sbp 131 --pred-all-dbp 80 \
  --pred-day-sbp 134 --pred-day-dbp 82 \
  --pred-night-sbp 128 --pred-night-dbp 74 \
  --out-dir eval_1p0_sensitivity/classification_custom_thr

# Load thresholds from grid-search output
python bp_htn_classification_multi_run_custom_thresholds.py \
  --csv-glob "eval_1p0_sensitivity/predictions_*.csv" \
  --methods bias \
  --threshold-csv threshold_search/threshold_grid_best_by_split.csv \
  --out-dir eval_1p0_sensitivity/classification_from_grid
"""

from __future__ import annotations

import argparse
import glob
import json
from dataclasses import dataclass, asdict
from pathlib import Path
from typing import Any, Dict, List, Optional, Sequence, Tuple

import numpy as np
import pandas as pd

TRUEY = {"1", "true", "yes", "y", "t"}


@dataclass(frozen=True)
class Threshold:
    sbp: float
    dbp: float


METHOD_TO_COLS: Dict[str, Tuple[str, str]] = {
    "raw": ("y_pred_sbp_raw", "y_pred_dbp_raw"),
    "bias": ("y_pred_sbp_bias", "y_pred_dbp_bias"),
    "aff": ("y_pred_sbp_aff", "y_pred_dbp_aff"),
    "bank": ("y_pred_sbp_bank", "y_pred_dbp_bank"),
    "cal": ("y_pred_sbp_cal", "y_pred_dbp_cal"),
}


def parse_bool_series(s: pd.Series) -> pd.Series:
    if s.dtype == bool:
        return s
    return s.astype(str).str.strip().str.lower().isin(TRUEY)


def safe_div(num: float, den: float) -> float:
    return 0.0 if den == 0 else float(num) / float(den)


def is_positive(sbp: float, dbp: float, thr: Threshold) -> bool:
    return (float(sbp) >= float(thr.sbp)) or (float(dbp) >= float(thr.dbp))


def confusion(y_true: Sequence[bool], y_pred: Sequence[bool]) -> Dict[str, Any]:
    y_true = np.asarray(y_true, dtype=bool)
    y_pred = np.asarray(y_pred, dtype=bool)

    tp = int(np.sum(y_true & y_pred))
    tn = int(np.sum((~y_true) & (~y_pred)))
    fp = int(np.sum((~y_true) & y_pred))
    fn = int(np.sum(y_true & (~y_pred)))

    precision = safe_div(tp, tp + fp)
    recall = safe_div(tp, tp + fn)
    specificity = safe_div(tn, tn + fp)
    accuracy = safe_div(tp + tn, tp + tn + fp + fn)
    f1 = safe_div(2 * precision * recall, precision + recall)

    return {
        "tp": tp,
        "tn": tn,
        "fp": fp,
        "fn": fn,
        "precision": precision,
        "recall": recall,
        "sensitivity": recall,
        "specificity": specificity,
        "accuracy": accuracy,
        "f1": f1,
    }


def method_name(method: str) -> str:
    if ":" in method:
        return method.split(":")[0]
    return method


def resolve_method_cols(method: str, df: pd.DataFrame) -> Tuple[str, str]:
    """
    Supports:
      bias
      aff
      bank
      custom_name:pred_sbp_col:pred_dbp_col
    """
    method = method.strip()
    if ":" in method:
        parts = method.split(":")
        if len(parts) != 3:
            raise ValueError(
                "Custom method should be name:pred_sbp_col:pred_dbp_col, "
                f"got {method}"
            )
        sbp_col, dbp_col = parts[1], parts[2]
    else:
        if method not in METHOD_TO_COLS:
            raise ValueError(f"Unknown method={method}; valid={list(METHOD_TO_COLS)}")
        sbp_col, dbp_col = METHOD_TO_COLS[method]

    missing = [c for c in [sbp_col, dbp_col] if c not in df.columns]
    if missing:
        raise ValueError(
            f"Missing prediction columns for method={method}: {missing}. "
            f"Available columns include: {list(df.columns)[:30]}"
        )
    return sbp_col, dbp_col


def filter_split(df: pd.DataFrame, split: str, sleep_col: str) -> pd.DataFrame:
    if split == "all":
        return df
    if split == "day":
        return df[pd.to_numeric(df[sleep_col], errors="coerce").fillna(0).astype(int) == 0]
    if split == "night":
        return df[pd.to_numeric(df[sleep_col], errors="coerce").fillna(0).astype(int) == 1]
    raise ValueError(f"Unknown split={split}")


def prepare_df(
    df: pd.DataFrame,
    include_calib: bool,
    is_calib_col: str,
) -> pd.DataFrame:
    out = df.copy()
    if not include_calib and is_calib_col in out.columns:
        out = out[~parse_bool_series(out[is_calib_col])].copy()
    return out


def collect_valid(
    df: pd.DataFrame,
    split: str,
    subject_col: str,
    sleep_col: str,
    true_sbp_col: str,
    true_dbp_col: str,
    pred_sbp_col: str,
    pred_dbp_col: str,
) -> pd.DataFrame:
    d = filter_split(df, split, sleep_col).copy()
    required = [subject_col, true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col]
    miss = [c for c in required if c not in d.columns]
    if miss:
        raise ValueError(f"Missing required columns: {miss}")

    for c in [true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col]:
        d[c] = pd.to_numeric(d[c], errors="coerce")

    d = d.dropna(subset=[subject_col, true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col])
    return d


def compute_subject_metrics(
    df: pd.DataFrame,
    split: str,
    true_thr: Threshold,
    pred_thr: Threshold,
    subject_col: str,
    sleep_col: str,
    true_sbp_col: str,
    true_dbp_col: str,
    pred_sbp_col: str,
    pred_dbp_col: str,
) -> Tuple[Dict[str, Any], Dict[str, Tuple[bool, bool, int]]]:
    d = collect_valid(df, split, subject_col, sleep_col, true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col)
    y_true: List[bool] = []
    y_pred: List[bool] = []
    labels: Dict[str, Tuple[bool, bool, int]] = {}

    for sid, g in d.groupby(subject_col, sort=False):
        true_sbp = float(g[true_sbp_col].mean())
        true_dbp = float(g[true_dbp_col].mean())
        pred_sbp = float(g[pred_sbp_col].mean())
        pred_dbp = float(g[pred_dbp_col].mean())
        t = is_positive(true_sbp, true_dbp, true_thr)
        p = is_positive(pred_sbp, pred_dbp, pred_thr)
        y_true.append(t)
        y_pred.append(p)
        labels[str(sid)] = (t, p, int(len(g)))

    m = confusion(y_true, y_pred)
    m.update({"n_rows": int(len(d)), "n_subjects": int(len(y_true)), "mode": "subject"})
    return m, labels


def compute_micro_metrics(
    df: pd.DataFrame,
    split: str,
    true_thr: Threshold,
    pred_thr: Threshold,
    subject_col: str,
    sleep_col: str,
    true_sbp_col: str,
    true_dbp_col: str,
    pred_sbp_col: str,
    pred_dbp_col: str,
) -> Dict[str, Any]:
    d = collect_valid(df, split, subject_col, sleep_col, true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col)
    y_true = [is_positive(r[true_sbp_col], r[true_dbp_col], true_thr) for _, r in d.iterrows()]
    y_pred = [is_positive(r[pred_sbp_col], r[pred_dbp_col], pred_thr) for _, r in d.iterrows()]
    m = confusion(y_true, y_pred)
    m.update({"n_rows": int(len(d)), "n_subjects": int(d[subject_col].nunique()), "mode": "micro"})
    return m


def compute_macro_metrics(
    df: pd.DataFrame,
    split: str,
    true_thr: Threshold,
    pred_thr: Threshold,
    subject_col: str,
    sleep_col: str,
    true_sbp_col: str,
    true_dbp_col: str,
    pred_sbp_col: str,
    pred_dbp_col: str,
) -> Dict[str, Any]:
    d = collect_valid(df, split, subject_col, sleep_col, true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col)

    total_true: List[bool] = []
    total_pred: List[bool] = []
    per_subject: List[Dict[str, Any]] = []

    for _, g in d.groupby(subject_col, sort=False):
        y_true = [is_positive(r[true_sbp_col], r[true_dbp_col], true_thr) for _, r in g.iterrows()]
        y_pred = [is_positive(r[pred_sbp_col], r[pred_dbp_col], pred_thr) for _, r in g.iterrows()]
        total_true.extend(y_true)
        total_pred.extend(y_pred)
        per_subject.append(confusion(y_true, y_pred))

    total = confusion(total_true, total_pred)
    if per_subject:
        for k in ["precision", "recall", "sensitivity", "specificity", "accuracy", "f1"]:
            total[k] = float(np.mean([x[k] for x in per_subject]))

    total.update({"n_rows": int(len(d)), "n_subjects": int(d[subject_col].nunique()), "mode": "macro"})
    return total


def compute_split_metrics(
    df: pd.DataFrame,
    split: str,
    average: str,
    true_thr: Threshold,
    pred_thr: Threshold,
    subject_col: str,
    sleep_col: str,
    true_sbp_col: str,
    true_dbp_col: str,
    pred_sbp_col: str,
    pred_dbp_col: str,
) -> Tuple[Dict[str, Any], Optional[Dict[str, Tuple[bool, bool, int]]]]:
    if average == "subject":
        return compute_subject_metrics(df, split, true_thr, pred_thr, subject_col, sleep_col, true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col)
    if average == "micro":
        return compute_micro_metrics(df, split, true_thr, pred_thr, subject_col, sleep_col, true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col), None
    if average == "macro":
        return compute_macro_metrics(df, split, true_thr, pred_thr, subject_col, sleep_col, true_sbp_col, true_dbp_col, pred_sbp_col, pred_dbp_col), None
    raise ValueError(f"Unknown average={average}")


def compute_combined_from_subject_labels(
    split_labels: Dict[str, Dict[str, Tuple[bool, bool, int]]]
) -> Dict[str, Any]:
    subject_ids = set()
    for labels in split_labels.values():
        subject_ids.update(labels.keys())

    y_true: List[bool] = []
    y_pred: List[bool] = []
    n_rows = 0

    for sid in sorted(subject_ids):
        t = False
        p = False
        for split in ["all", "day", "night"]:
            if sid in split_labels.get(split, {}):
                tt, pp, nn = split_labels[split][sid]
                t = t or tt
                p = p or pp
                n_rows += int(nn)
        y_true.append(t)
        y_pred.append(p)

    m = confusion(y_true, y_pred)
    m.update({"n_rows": int(n_rows), "n_subjects": int(len(subject_ids)), "mode": "subject"})
    return m


