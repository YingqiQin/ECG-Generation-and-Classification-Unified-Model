def build_true_thresholds(args: argparse.Namespace) -> Dict[str, Threshold]:
    return {
        "all": Threshold(args.true_all_sbp, args.true_all_dbp),
        "day": Threshold(args.true_day_sbp, args.true_day_dbp),
        "night": Threshold(args.true_night_sbp, args.true_night_dbp),
    }


def build_cli_pred_thresholds(args: argparse.Namespace) -> Dict[str, Threshold]:
    true_thr = build_true_thresholds(args)

    def get(split: str, sbp: Optional[float], dbp: Optional[float]) -> Threshold:
        return Threshold(
            true_thr[split].sbp if sbp is None else float(sbp),
            true_thr[split].dbp if dbp is None else float(dbp),
        )

    return {
        "all": get("all", args.pred_all_sbp, args.pred_all_dbp),
        "day": get("day", args.pred_day_sbp, args.pred_day_dbp),
        "night": get("night", args.pred_night_sbp, args.pred_night_dbp),
    }


def load_threshold_csv(path: Optional[str]) -> Dict[Tuple[str, str], Threshold]:
    if path is None:
        return {}
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"threshold csv not found: {path}")
    df = pd.read_csv(p)
    required = ["method", "split", "pred_sbp_thr", "pred_dbp_thr"]
    miss = [c for c in required if c not in df.columns]
    if miss:
        raise ValueError(f"threshold csv missing columns: {miss}")

    out: Dict[Tuple[str, str], Threshold] = {}
    for _, r in df.iterrows():
        method = str(r["method"])
        split = str(r["split"])
        if split not in {"all", "day", "night"}:
            continue
        out[(method, split)] = Threshold(float(r["pred_sbp_thr"]), float(r["pred_dbp_thr"]))
    return out


def get_pred_thr(method: str, split: str, cli_pred: Dict[str, Threshold], loaded: Dict[Tuple[str, str], Threshold]) -> Threshold:
    m = method_name(method)
    if (m, split) in loaded:
        return loaded[(m, split)]
    # Compatibility with sleep-shrinkage grid script.
    if m == "bias" and ("bias_sleep_shrink", split) in loaded:
        return loaded[("bias_sleep_shrink", split)]
    return cli_pred[split]


